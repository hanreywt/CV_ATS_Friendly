export function generateLatex(resume: Resume): string {
  const sections = [
    buildHeading(resume),
    buildSummary(resume),
    buildExperience(resume),
    buildProjects(resume),
    buildEducation(resume),
    buildSkills(resume),
    buildHonors(resume),
  ]
    .filter(Boolean)
    .join('\n\n');

  return `${PREAMBLE}\\begin{document}

${sections}

\\end{document}
`;
}
